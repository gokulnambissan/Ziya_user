        children: [
          pw.Expanded(
            child: pw.Text(
              'Gross Earnings: ₹ ${payslip.grossEarnings.toStringAsFixed(0)}',
              style: pw.TextStyle(fontWeight: pw.FontWeight.bold, fontSize: 10),
            ),
          ),
          pw.Expanded(
            child: pw.Text(
              'Total Deductions: ₹ ${payslip.totalDeductions.toStringAsFixed(0)}',
              style: pw.TextStyle(fontWeight: pw.FontWeight.bold, fontSize: 10),
            ),
          ),
        ],
      ),
    );
  }

